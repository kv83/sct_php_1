<h1> Мишка Шифман </h1>
<hr> Мишка Шифман башковит — <br>
У его предвиденье.<br>
«Что мы видим, — говорит, —<br>
Кроме телевиденья?!<br>
Смотришь конкурс в Сопоте —<br>
И глотаешь пыль,<br>
А кого ни попадя<br>
Пускают в Израиль!» <hr>

